export const getTojeong = (birthDate, currentYear) => {
    // 생년월일과 올해의 숫자를 조합하여 0-7 사이의 인덱스 생성
    const year = parseInt(currentYear.toString().slice(-2));
    const month = birthDate.getMonth() + 1;
    const day = birthDate.getDate();
    const resultIndex = (year + month + day) % 8;

    const result = results[resultIndex];

    // 월별 운세 생성
    const monthly = Array.from({ length: 12 }, (_, i) => {
        const monthIndex = (resultIndex + i) % 12;
        return { month: i + 1, fortune: monthlyFortune[monthIndex] };
    });

    return {
        general: result.general,
        action: result.action, // 행동 지침 추가
        monthly: monthly
    };
};